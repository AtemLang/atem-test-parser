    case 163: RightCurlyAction(antlrcpp::downCast<antlr4::RuleContext *>(context), actionIndex); break;
    case 255: InterpolataionSingleLineAction(antlrcpp::downCast<antlr4::RuleContext *>(context), actionIndex); break;
