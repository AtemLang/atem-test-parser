void AtemLexer::action(RuleContext *context, size_t ruleIndex, size_t actionIndex) {
  switch (ruleIndex) {
    case 158: LeftCurlyAction(antlrcpp::downCast<antlr4::RuleContext *>(context), actionIndex); break;
    case 159: RightCurlyAction(antlrcpp::downCast<antlr4::RuleContext *>(context), actionIndex); break;
    case 250: InterpolataionSingleLineAction(antlrcpp::downCast<antlr4::RuleContext *>(context), actionIndex); break;
    case 253: InterpolataionMultiLineAction(antlrcpp::downCast<antlr4::RuleContext *>(context), actionIndex); break;

  default:
    break;
  }
}

void AtemLexer::LeftCurlyAction(antlr4::RuleContext *context, size_t actionIndex) {
  switch (actionIndex) {
    case 0:  
    					if (!curly.empty()) {
    						int top = curly.top();
    						curly.pop();
    						curly.push(top + 1);
    						curly.pop();
    					}
    				 break;

  default:
    break;
  }
}

void AtemLexer::RightCurlyAction(antlr4::RuleContext *context, size_t actionIndex) {
  switch (actionIndex) {
    case 1:  if(!curly.empty()) 
    					{
    						int top = curly.top();
    						curly.pop();
    						curly.push(top - 1); 
    						if(curly.top() == 0) 
    						{ 
    							curly.pop();
    							popMode();
    						}
    					}
    				 break;

  default:
    break;
  }
}

